
class Employee:
    def getGivenName(self):
        return self._given
    def setGivenName(self, value):
        self._given = value

    def getFamilyName(self):
        return self._family
    def setFamilyName(self, value):
        self._family = value

    def getDateOfBirth(self):
        return self._birth
    def setDateOfBirth(self, value):
        self._birth = value

